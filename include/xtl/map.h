///////////////////////////////////////////////////////////////////////////////
//
//	Description: Contains composite functions for the STL hash_map class
//
///////////////////////////////////////////////////////////////////////////////
#pragma once
///////////////////////////////////////////////////////////////////////////////
#include "objio.h"
#include "utility.h"
#include <map>
///////////////////////////////////////////////////////////////////////////////
template <class Stream, class T1, class T2, class F, class A>
inline void composite(Stream& stream, std::map<T1, T2, F, A>& data) {
	stream.map(data);
}
///////////////////////////////////////////////////////////////////////////////
template <class Stream, class T1, class T2, class F, class A>
inline void composite(Stream& stream, std::multimap<T1, T2, F, A>& data) {
	stream.map(data);
}
