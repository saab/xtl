///////////////////////////////////////////////////////////////////////////////
//
//	Description: Contains composite functions for STL bitset
//
///////////////////////////////////////////////////////////////////////////////
#pragma once
///////////////////////////////////////////////////////////////////////////////
#include "objio.h"
#include <bitset>
///////////////////////////////////////////////////////////////////////////////
template<typename TFormat, std::size_t nBits>
void composite(obj_input<TFormat>& stream, std::bitset<nBits>& bs)
{
	unsigned long ul;
	stream.simple(ul);
	bs = std::bitset<nBits>(ul);
}
///////////////////////////////////////////////////////////////////////////////
template<typename TFormat, std::size_t nBits>
void composite(obj_output<TFormat>& stream, std::bitset<nBits>& bs)
{
	stream.simple(bs.to_ulong());
}
