///////////////////////////////////////////////////////////////////////////////
//
//	Description: Contains composite functions for the STL set class
//
///////////////////////////////////////////////////////////////////////////////
#pragma once
///////////////////////////////////////////////////////////////////////////////
#include "objio.h"
#include <set>
///////////////////////////////////////////////////////////////////////////////
template <class Stream, class T, class F, class A>
inline void composite(Stream& stream, std::set<T, F, A>& data) {
	stream.container(data);
}
///////////////////////////////////////////////////////////////////////////////
template <class Stream, class T, class F, class A>
inline void composite(Stream& stream, std::multiset<T, F, A>& data) {
	stream.container(data);
}
