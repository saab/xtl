///////////////////////////////////////////////////////////////////////////////
#pragma once
///////////////////////////////////////////////////////////////////////////////
#include "objio.h"
#include <boost/scoped_ptr.hpp>
///////////////////////////////////////////////////////////////////////////////
template<class Format, typename T>
inline void composite(obj_input<Format>& stream, boost::scoped_ptr<T>& p)
{
	T* pt = 0;
	stream.pointer(pt);
	p.reset(pt);
}
///////////////////////////////////////////////////////////////////////////////
template<class Format, typename T>
inline void composite(obj_output<Format>& stream, boost::scoped_ptr<T>& p)
{
	stream.pointer(p.get());
}
